// ─── Helpers ───
const DownloadBtn = ({ data, filename }) => {
  const download = () => {
    const md = `# ${filename.replace(/_/g, ' ').toUpperCase()}\n\nGenerated by Career Twin\n\n`;
    const body = JSON.stringify(data, null, 2);
    const blob = new Blob([md + '```json\n' + body + '\n```'], { type: 'text/markdown' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${filename}.md`;
    a.click();
    URL.revokeObjectURL(url);
  };
  return (
    <button onClick={download} className="px-3 py-1.5 bg-gray-100 hover:bg-gray-200 text-gray-700 rounded-lg text-xs font-semibold transition">
      ⬇️ Download Markdown
    </button>
  );
};

const CopyBtn = ({ text, label = '📋 Copy' }) => {
  const [ok, setOk] = useState(false);
  const copy = () => {
    navigator.clipboard.writeText(text);
    setOk(true);
    setTimeout(() => setOk(false), 2000);
  };
  return (
    <button onClick={copy} className="px-3 py-1.5 bg-indigo-50 hover:bg-indigo-100 text-indigo-700 rounded-lg text-xs font-semibold transition">
      {ok ? '✅ Copied!' : label}
    </button>
  );
};

