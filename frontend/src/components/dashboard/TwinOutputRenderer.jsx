// ─── Error Renderer ───
const ErrorRenderer = ({ data }) => (
  <div className="bg-rose-50 border border-rose-200 rounded-xl p-5 text-center">
    <p className="text-2xl mb-2">⚠️</p>
    <h4 className="text-sm font-bold text-rose-900 mb-1">Career Twin Encountered an Issue</h4>
    <p className="text-xs text-rose-700 mb-3">{data.text || data.summary || 'Unknown error'}</p>
    <p className="text-[11px] text-rose-600 bg-white/60 rounded p-2">
      Fallback mode activated. The output above is generated from your profile without AI — it's still actionable.
    </p>
  </div>
);

// ─── Main Router ───
const TwinOutputRenderer = ({ result }) => {
  if (!result) return null;

  if (result.type === 'error') {
    return <ErrorRenderer data={result} />;
  }

  const action = result.actions?.[0];
  if (!action) {
    return (
      <div className="bg-gray-50 rounded-xl p-6 text-center text-sm text-gray-500">
        No actionable output found. Try a more specific command.
      </div>
    );
  }

  switch (action.tool) {
    case 'linkedin_post':
      return <LinkedInRenderer data={action} />;
    case 'portfolio_brief':
      return <PortfolioRenderer data={action} />;
    case 'interview_sim':
      return <InterviewRenderer data={action} />;
    case 'immunization_plan':
      return <ImmunizationRenderer data={action} />;
    default:
      return (
        <div className="bg-white rounded-xl border border-gray-200 p-5">
          <h4 className="font-bold text-gray-900 mb-2">General Output</h4>
          <p className="text-sm text-gray-700 whitespace-pre-line">{result.summary}</p>
          <div className="mt-4">
            <DownloadBtn data={result} filename="career_twin_output" />
          </div>
        </div>
      );
  }
};

export default TwinOutputRenderer;